interface EmojiPickerProps {
  selectedEmoji: string;
  onSelectEmoji: (emoji: string) => void;
  color: string;
}

export function EmojiPicker({ selectedEmoji, onSelectEmoji, color }: EmojiPickerProps) {
  const [searchQuery, setSearchQuery] = useState('');
  const [activeCategory, setActiveCategory] = useState<string>(Object.keys(EMOJI_CATEGORIES)[0]);

  // Filter emojis based on search
  const getFilteredEmojis = () => {
    if (!searchQuery.trim()) {
      return EMOJI_CATEGORIES[activeCategory as keyof typeof EMOJI_CATEGORIES] || [];
    }

    // Search across all categories
    const allEmojis: string[] = [];
    Object.values(EMOJI_CATEGORIES).forEach(emojis => {
      allEmojis.push(...emojis);
    });
    
    // For now, just return all emojis if searching (emoji search by name would require a mapping)
    return allEmojis;
  };

  const filteredEmojis = getFilteredEmojis();
  const categories = Object.keys(EMOJI_CATEGORIES);

  return (
    <View style={styles.container}>
      {/* Search Bar */}
      <TextInput
        style={styles.searchInput}
        value={searchQuery}
        onChangeText={setSearchQuery}
        placeholder="Search emojis..."
        placeholderTextColor={colors.textSecondary}
      />

      {/* Category Tabs */}
      {!searchQuery && (
        <ScrollView
          horizontal
          showsHorizontalScrollIndicator={false}
          style={styles.categoryScroll}
          contentContainerStyle={styles.categoryScrollContent}
        >
          {categories.map((category) => {
            const isActive = category === activeCategory;
            return (
              <TouchableOpacity
                key={category}
                style={[
                  styles.categoryTab,
                  isActive && { backgroundColor: color, borderColor: color }
                ]}
                onPress={() => setActiveCategory(category)}
              >
                <Text style={[
                  styles.categoryText,
                  isActive && styles.categoryTextActive
                ]}>
                  {category}
                </Text>
              </TouchableOpacity>
            );
          })}
        </ScrollView>
      )}

      {/* Emoji Grid */}
      <ScrollView style={styles.emojiScroll} showsVerticalScrollIndicator={false}>
        <View style={styles.emojiGrid}>
          {filteredEmojis.map((emoji, index) => {
            const isSelected = emoji === selectedEmoji;
            return (
              <TouchableOpacity
                key={`${emoji}-${index}`}
                style={[
                  styles.emojiButton,
                  isSelected && { backgroundColor: color, borderColor: color, borderWidth: 3 }
                ]}
                onPress={() => onSelectEmoji(emoji)}
              >
                <Text style={styles.emoji}>{emoji}</Text>
              </TouchableOpacity>
            );
          })}
        </View>
      </ScrollView>
    </View>
  );
}

